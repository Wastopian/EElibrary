/** octopartProviderAdapter fetches and normalizes Octopart supply data through Nexar GraphQL. */
export const octopartProviderAdapter: ProviderAdapter = {
  async findExactPartCandidates(request) {
    if (!hasConfiguredOctopartCredentials()) {
      return [];
    }

    try {
      const rawPayload = await fetchOctopartRawPart({
        ...(request.manufacturerName ? { manufacturerName: request.manufacturerName } : {}),
        mpn: request.query
      });
      const normalizedPart = normalizeRawPart(rawPayload);

      return [buildExactLookupCandidate(normalizedPart, request.query)];
    } catch (error) {
      if (isOctopartNotFoundError(error) || isOctopartCredentialError(error)) {
        return [];
      }

      throw error;
    }
  },
  async fetchRawPart(request) {
    return fetchOctopartRawPart(request);
  },
  id: OCTOPART_PROVIDER_ID,
  async listAvailablePartRequests() {
    return [];
  },
  name: "Octopart via Nexar GraphQL",
  normalizeRawPart
};

